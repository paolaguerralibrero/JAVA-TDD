public class Calculator {

    public int add(int paramA, int paramB) {
        return paramA + paramB;
    }

    public int subtract(int paramA, int paramB) {
        return paramA - paramB;
    }

    public int multiply(int paramA, int paramB) {
        return paramA * paramB;
    }

    public double divide(double paramA, double paramB) {
        return paramA / paramB;
    }
}
